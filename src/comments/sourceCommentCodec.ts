// Pure source-comment scanner and encoder. The scanner deliberately accepts
// only comments whose delimiter is the first token after indentation. This
// avoids treating ordinary code and most string literals as comments while
// remaining useful across languages without embedding a full parser.

export type SourceCommentKind = 'header' | 'line' | 'block' | 'docblock';

export interface SourceCommentRecord {
    /** Inclusive, 0-based first source line. */
    readonly startLine: number;
    /** Inclusive, 0-based last source line. */
    readonly endLine: number;
    /** Comment decorations removed; logical lines are joined with `\n`. */
    readonly text: string;
    readonly kind: SourceCommentKind;
    /**
     * Safe annotation-id fragment carried by comments generated by OOCI.
     * The marker is removed from `text` so a generated comment can round-trip
     * without creating an annotation whose message starts with `OOCI(...)`.
     */
    readonly annotationIdFragment?: string;
}

export type SourceCommentEncodingStyle = 'auto' | 'line' | 'block' | 'docblock';

export interface EncodeSourceCommentOptions {
    /** Stable annotation id. Only a safe, short fragment is emitted. */
    readonly annotationId: string;
    /** Spaces/tabs to place before every generated comment line. */
    readonly indentation?: string;
    /** `auto` prefers a line comment and otherwise uses a block. */
    readonly style?: SourceCommentEncodingStyle;
}

interface LineCommentStyle {
    readonly prefix: string;
    readonly kind: 'line' | 'docblock';
}

interface BlockCommentStyle {
    readonly open: string;
    readonly close: string;
    readonly kind: 'block' | 'docblock';
    /** Conventional leading `*` on each body line. */
    readonly starBody?: boolean;
    /** Additional body sequences forbidden by the language grammar. */
    readonly forbiddenBodySequences?: readonly string[];
}

interface LanguageCommentSyntax {
    readonly lines: readonly LineCommentStyle[];
    readonly blocks: readonly BlockCommentStyle[];
    /** Sequences unsafe even inside line comments (notably PHP's closing tag). */
    readonly forbiddenMessageSequences?: readonly string[];
}

